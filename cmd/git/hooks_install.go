package git

import (
	"context"
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/spf13/cobra"
	"github.com/spf13/viper"

	errUtils "github.com/cloudposse/atmos/errors"
	"github.com/cloudposse/atmos/pkg/flags"
	atmosgit "github.com/cloudposse/atmos/pkg/git"
	"github.com/cloudposse/atmos/pkg/perf"
	"github.com/cloudposse/atmos/pkg/schema"
	"github.com/cloudposse/atmos/pkg/ui"
)

const (
	// FlagForce is the flag name for --force on hooks install.
	flagForce = "force"

	// AtmosShimMarker is the unique string embedded in generated shim files.
	// Its presence identifies a file as Atmos-managed.
	atmosShimMarker = "# Generated by Atmos. Do not edit."

	// ShimPerm is the file permission for generated hook shims (rwxr-xr-x).
	shimPerm = 0o755

	// HooksDirPerm is the directory permission for the .git/hooks directory.
	hooksDirPerm = 0o755
)

// hooksInstallParser handles flag parsing for `atmos git hooks install`.
var hooksInstallParser = flags.NewStandardParser(
	flags.WithBoolFlag(flagForce, "f", false, "Overwrite existing hook files"),
	flags.WithEnvVars(flagForce, "ATMOS_GIT_HOOKS_FORCE"),
)

// hooksInstallCmd is the `atmos git hooks install` subcommand.
var hooksInstallCmd = &cobra.Command{
	Use:   "install [hook-name...]",
	Short: "Install Atmos-managed Git hook shims",
	Long: `Write shim scripts into .git/hooks for each configured git.hooks entry.

Without hook names, installs all configured hooks.
With hook names, installs only the named hooks (error if not configured).

The shim content is:

  #!/bin/sh
  # Generated by Atmos. Do not edit.
  exec atmos git hooks run <hook> "$@"

Refuses to overwrite an existing hook unless --force is passed, unless the
existing file already contains the Atmos marker.

Warns (but does not fail) when core.hooksPath is set, because Git will ignore
.git/hooks shims in that case.`,
	RunE: func(cmd *cobra.Command, args []string) error {
		defer perf.Track(atmosConfigPtr, "git.hooks.install.RunE")()

		v := viper.GetViper()
		if err := hooksInstallParser.BindFlagsToViper(cmd, v); err != nil {
			return err
		}

		force := v.GetBool(flagForce)
		return runHooksInstall(cmd.Context(), args, force)
	},
}

// runHooksInstall implements `atmos git hooks install`.
func runHooksInstall(ctx context.Context, names []string, force bool) error {
	defer perf.Track(nil, "git.runHooksInstall")()

	cfg := gitConfig()

	// Validate requested names against config before touching the filesystem.
	if err := validateHookNames(names, cfg); err != nil {
		return err
	}

	// Warn when core.hooksPath is set; Git ignores .git/hooks in that case.
	warnIfHooksPathSet(ctx)

	hooksDir, err := resolveHooksDir(ctx)
	if err != nil {
		return err
	}

	// Determine which hooks to install.
	hookNames := names
	if len(hookNames) == 0 && cfg != nil {
		hookNames = sortedKeys(cfg.Hooks)
	}

	if len(hookNames) == 0 {
		ui.Info("No hooks configured under git.hooks in atmos.yaml.")
		return nil
	}

	for _, name := range hookNames {
		if err := installHook(hooksDir, name, force); err != nil {
			return err
		}
	}

	return nil
}

// installHook writes a single shim file for hookName into hooksDir.
func installHook(hooksDir, hookName string, force bool) error {
	if err := validateHookShimName(hookName); err != nil {
		return err
	}

	dest := filepath.Join(hooksDir, hookName)
	shim := shimContent(hookName)

	// Check if the file already exists.
	existing, err := os.ReadFile(dest)
	if err == nil {
		// File exists; overwrite only if Atmos-managed or --force is set.
		if !strings.Contains(string(existing), atmosShimMarker) && !force {
			return errUtils.Build(errUtils.ErrInvalidConfig).
				WithHintf("Hook file %q already exists and was not generated by Atmos.", dest).
				WithHint("Pass --force to overwrite it.").
				WithExitCode(2).
				Err()
		}
	} else if !os.IsNotExist(err) {
		return fmt.Errorf("checking existing hook %q: %w", dest, err)
	}

	// Ensure the hooks directory exists.
	if mkErr := os.MkdirAll(hooksDir, hooksDirPerm); mkErr != nil {
		return fmt.Errorf("creating hooks directory %q: %w", hooksDir, mkErr)
	}

	if writeErr := os.WriteFile(dest, []byte(shim), shimPerm); writeErr != nil {
		return fmt.Errorf("writing hook shim %q: %w", dest, writeErr)
	}

	ui.Successf("Installed hook shim: %s", dest)
	return nil
}

// shimContent returns the generated shim content for a given hook name.
func shimContent(hookName string) string {
	return "#!/bin/sh\n" + atmosShimMarker + "\nexec atmos git hooks run " + hookName + " \"$@\"\n"
}

// resolveHooksDir returns the absolute path to the .git/hooks directory,
// correctly handling linked worktrees where hooks live in the common dir.
func resolveHooksDir(ctx context.Context) (string, error) {
	defer perf.Track(nil, "git.resolveHooksDir")()

	runner := atmosgit.NewExecRunner()
	result, err := runner.Run(ctx, "git", []string{"rev-parse", "--git-path", "hooks"}, atmosgit.RunOptions{})
	if err != nil {
		return "", errUtils.Build(errUtils.ErrNotInGitRepository).
			WithHint("Run 'atmos git hooks install' from inside a Git repository.").
			WithExitCode(2).
			Err()
	}

	dir := strings.TrimSpace(result.Stdout)
	if dir == "" {
		return "", errUtils.Build(errUtils.ErrNotInGitRepository).
			WithHint("'git rev-parse --git-path hooks' returned an empty path.").
			WithExitCode(2).
			Err()
	}

	abs, err := filepath.Abs(dir)
	if err != nil {
		return "", fmt.Errorf("resolving hooks directory path %q: %w", dir, err)
	}

	return abs, nil
}

// warnIfHooksPathSet emits a ui.Warning when core.hooksPath is configured,
// because Git ignores .git/hooks when that setting is present.
// Returns without warning when core.hooksPath is not configured.
func warnIfHooksPathSet(ctx context.Context) {
	runner := atmosgit.NewExecRunner()
	result, err := runner.Run(ctx, "git", []string{"config", "--get", "core.hooksPath"}, atmosgit.RunOptions{})
	if err != nil {
		// Non-zero exit from git config --get means the key is not set.
		return
	}

	hooksPath := strings.TrimSpace(result.Stdout)
	if hooksPath != "" {
		ui.Warningf(
			"core.hooksPath is set to %q (e.g. by Husky). Git will ignore .git/hooks shims; install them into %q instead, or remove core.hooksPath.",
			hooksPath, hooksPath,
		)
	}
}

// validateHookNames ensures all requested hook names are configured.
func validateHookNames(names []string, cfg *schema.GitConfig) error {
	if len(names) == 0 {
		return nil
	}

	for _, name := range names {
		if cfg == nil || cfg.Hooks == nil {
			return hookNotConfiguredErr(name, nil)
		}
		if _, ok := cfg.Hooks[name]; !ok {
			return hookNotConfiguredErr(name, cfg.Hooks)
		}
	}

	return nil
}

func init() {
	hooksInstallParser.RegisterFlags(hooksInstallCmd)
	if err := hooksInstallParser.BindToViper(viper.GetViper()); err != nil {
		panic(fmt.Sprintf("git hooks install: BindToViper: %v", err))
	}
}
